void	put_str_ptr(char *str)
{
	while (*str != '\0')
		write(1, str++, 1);
}

void	put_str(char *str)
{
	unsigned int s;

	s = 0;
	while (str[s] != '\0')
		write(1, &str[s++], 1);
}
int		main()
{
	char s[2];

	s[0] = 'b';
	s[1] = '\0';
	put_str(s);
	*(s + 0) = 'z';
	*(s + 1) = '\0';
	put_str_ptr(s);
}
